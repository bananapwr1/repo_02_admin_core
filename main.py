# --- –ù–∞—Å—Ç—Ä–æ–π–∫–∞ ---
load_dotenv()
logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

TELEGRAM_BOT_TOKEN_ADMIN = os.getenv("TELEGRAM_BOT_TOKEN_ADMIN")
SUPABASE_URL = os.getenv("SUPABASE_URL")
SUPABASE_KEY = os.getenv("SUPABASE_KEY_FOR_ADMIN") 

supabase: Optional[Client] = create_client(SUPABASE_URL, SUPABASE_KEY)

# --- –õ–æ–≥–∏–∫–∞ –ò–ò –∏ –ì–µ–Ω–µ—Ä–∞—Ü–∏–∏ –°—Ç—Ä–∞—Ç–µ–≥–∏–∏ (–ù–û–í–ê–Ø –§–£–ù–ö–¶–ò–Ø) ---

async def generate_new_strategy():
    """
    –¢–Ø–ñ–ï–õ–´–ô –ü–†–û–¶–ï–°–°: –ê–Ω–∞–ª–∏–∑ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ Supabase –∏ –≥–µ–Ω–µ—Ä–∞—Ü–∏—è –Ω–æ–≤–æ–≥–æ –∞–ª–≥–æ—Ä–∏—Ç–º–∞.
    –≠—Ç–∞ —Ñ—É–Ω–∫—Ü–∏—è –∑–∞–ø—É—Å–∫–∞–µ—Ç—Å—è –ø–æ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—é –∏–ª–∏ –∫–æ–º–∞–Ω–¥–µ.
    """
    logger.info("üß† Running AI analysis and strategy generation...")
    if not supabase: return

    try:
        # 1. –ß—Ç–µ–Ω–∏–µ –ø—Ä–æ—à–ª—ã—Ö —Ç—Ä–µ–π–¥–æ–≤ –∏ —Å–∏–≥–Ω–∞–ª–æ–≤ –∏–∑ Supabase
        # response = supabase.table("trades").select("*").limit(100).execute()
        # past_data = pd.DataFrame(response.data)
        
        # 2. –ò–ò-–∞–Ω–∞–ª–∏–∑ (–ª–æ–≥–∏–∫–∞ ML –∏–ª–∏ LLM) - –∑–¥–µ—Å—å –±—É–¥–µ—Ç —Å–∞–º–∞—è –±–æ–ª—å—à–∞—è –Ω–∞–≥—Ä—É–∑–∫–∞
        # new_algorithm_rules = run_heavy_ai_model(past_data)

        # 3. –ì–µ–Ω–µ—Ä–∞—Ü–∏—è –Ω–æ–≤–æ–π —Å—Ç—Ä–∞—Ç–µ–≥–∏–∏
        new_strategy = {
            "name": f"Strategy_{int(time.time())}",
            "is_active": True,
            "assets_to_monitor": ["EURUSD=X", "GBPJPY=X"],
            "default_amount": 15.0,
            "default_timeframe": 120,
            "algorithm_rules": "RSI < 30 AND MACD Cross UP", # –ß–∏—Å—Ç—ã–π, –ª–µ–≥–∫–∏–π –∞–ª–≥–æ—Ä–∏—Ç–º –¥–ª—è –Ø–¥—Ä–∞
            "description": "Generated by AI based on last week's best performing assets."
        }

        # 4. –ó–∞–ø–∏—Å—å –≤ Supabase (–æ—Ç–∫–ª—é—á–∞–µ–º —Å—Ç–∞—Ä—ã–µ, –≤–∫–ª—é—á–∞–µ–º –Ω–æ–≤—É—é)
        supabase.table("strategy_settings").update({"is_active": False}).execute()
        supabase.table("strategy_settings").insert(new_strategy).execute()
        
        logger.info(f"‚úÖ New strategy '{new_strategy['name']}' deployed to Supabase.")

    except Exception as e:
        logger.error(f"‚ùå Error in AI strategy generation: {e}")

# --- Telegram Bot –õ–æ–≥–∏–∫–∞ ---

async def manage_strategy_command(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """–û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –∫–æ–º–∞–Ω–¥—É /ai_run (–∑–∞–ø—É—Å–∫ –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ –∞–ª–≥–æ—Ä–∏—Ç–º–∞)."""
    await update.message.reply_text("‚è≥ –ó–∞–ø—É—Å–∫–∞—é —Ç—è–∂–µ–ª—ã–π –ø—Ä–æ—Ü–µ—Å—Å AI-–∞–Ω–∞–ª–∏–∑–∞ –∏ –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ –Ω–æ–≤–æ–π —Å—Ç—Ä–∞—Ç–µ–≥–∏–∏. –≠—Ç–æ –º–æ–∂–µ—Ç –∑–∞–Ω—è—Ç—å –Ω–µ—Å–∫–æ–ª—å–∫–æ –º–∏–Ω—É—Ç...")
    
    # –ó–∞–ø—É—Å–∫–∞–µ–º —Ç—è–∂–µ–ª—ã–π –ø—Ä–æ—Ü–µ—Å—Å –≤ –æ—Ç–¥–µ–ª—å–Ω–æ–º –ø–æ—Ç–æ–∫–µ, —á—Ç–æ–±—ã –Ω–µ –±–ª–æ–∫–∏—Ä–æ–≤–∞—Ç—å Telegram
    await asyncio.to_thread(asyncio.run, generate_new_strategy()) 
    
    await update.message.reply_text("‚úÖ –ì–µ–Ω–µ—Ä–∞—Ü–∏—è —Å—Ç—Ä–∞—Ç–µ–≥–∏–∏ –∑–∞–≤–µ—Ä—à–µ–Ω–∞! –Ø–¥—Ä–æ Render –Ω–∞—á–Ω–µ—Ç –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –Ω–æ–≤—ã–π –∞–ª–≥–æ—Ä–∏—Ç–º –≤ —Å–ª–µ–¥—É—é—â–µ–º —Ü–∏–∫–ª–µ.")


# –ó–¥–µ—Å—å –±—É–¥—É—Ç –æ—Å—Ç–∞–ª—å–Ω—ã–µ –∞–¥–º–∏–Ω-–∫–æ–º–∞–Ω–¥—ã: /view_logs, /view_users, /block_user

def run_admin_bot():
    """–ó–∞–ø—É—Å–∫–∞–µ—Ç Telegram-–±–æ—Ç."""
    if not TELEGRAM_BOT_TOKEN_ADMIN:
        logger.error("üö´ TELEGRAM_BOT_TOKEN_ADMIN –Ω–µ –∑–∞–¥–∞–Ω.")
        return

    application = Application.builder().token(TELEGRAM_BOT_TOKEN_ADMIN).build()
    application.add_handler(CommandHandler("ai_run", manage_strategy_command))
    
    application.run_polling(poll_interval=1.0)


if __name__ == "__main__":
    run_admin_bot()

