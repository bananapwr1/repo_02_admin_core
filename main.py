async def status_command(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç —Å—Ç–∞—Ç—É—Å –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–π."""
    status_msg = "üìä **–°—Ç–∞—Ç—É—Å —Å–∏—Å—Ç–µ–º—ã:**\n\n"
    
    # –ü—Ä–æ–≤–µ—Ä–∫–∞ Telegram Bot
    status_msg += "‚úÖ Telegram Bot: –ü–æ–¥–∫–ª—é—á–µ–Ω\n"
    
    # –ü—Ä–æ–≤–µ—Ä–∫–∞ Supabase
    if supabase:
        try:
            # –ü—ã—Ç–∞–µ–º—Å—è —Å–¥–µ–ª–∞—Ç—å –ø—Ä–æ—Å—Ç–æ–π –∑–∞–ø—Ä–æ—Å
            response = supabase.table("strategy_settings").select("count", count="exact").execute()
            status_msg += f"‚úÖ Supabase: –ü–æ–¥–∫–ª—é—á–µ–Ω (—Å—Ç—Ä–∞—Ç–µ–≥–∏–π: {response.count or 0})\n"
        except Exception as e:
            status_msg += f"‚ö†Ô∏è Supabase: –û—à–∏–±–∫–∞ - {str(e)}\n"
    else:
        status_msg += "‚ùå Supabase: –ù–µ –ø–æ–¥–∫–ª—é—á–µ–Ω\n"
    
    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è
